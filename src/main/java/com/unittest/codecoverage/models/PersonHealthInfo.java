package com.unittest.codecoverage.models;

public class PersonHealthInfo {
	
	private double pas;
	private double pad;
	private double pam;
	
	public double getPas() {
		return pas;
	}
	public void setPas(double pas) {
		this.pas = pas;
	}
	public double getPad() {
		return pad;
	}
	public void setPad(double pad) {
		this.pad = pad;
	}
	public double getPam() {
		return pam;
	}
	public void setPam(double pam) {
		this.pam = pam;
	}
	
}
